/** 간지 문자열(예: "甲子")로 바로 접근하는 맵 */
export const ILJU_MULSANG: Record<string, IljuMulsang> = (() => {
  const map: Record<string, IljuMulsang> = {}
  for (const item of ILJU_MULSANG_DATA) {
    map[item.ganji] = item
  }
  return map
})()

/** 한글 간지(예: "갑자")로 접근하는 맵 */
export const ILJU_MULSANG_KR: Record<string, IljuMulsang> = (() => {
  const map: Record<string, IljuMulsang> = {}
  for (const item of ILJU_MULSANG_DATA) {
    map[item.ganjiKr] = item
  }
  return map
})()

/**
 * 일주 간지로 물상 데이터를 조회
 * @param ganji - 한자 간지 (예: "甲子") 또는 한글 간지 (예: "갑자")
 */
export function getIljuMulsang(ganji: string): IljuMulsang | null {
  return ILJU_MULSANG[ganji] || ILJU_MULSANG_KR[ganji] || null
}

/**
 * 일주 물상을 AI 프롬프트 텍스트로 변환
 */
export function buildIljuMulsangText(ganji: string): string {
  const data = getIljuMulsang(ganji)
  if (!data) return ''

  return [
    `### 60갑자 일주 물상(日柱 物象) — ${data.ganji}(${data.ganjiKr})`,
    `상징: ${data.symbol}`,
    `핵심 성격: ${data.personality}`,
    `강점: ${data.strengths.join(' / ')}`,
    `약점: ${data.weaknesses.join(' / ')}`,
    `적성 직업: ${data.career.join(', ')}`,
    `연애 스타일: ${data.lovePattern}`,
  ].join('\n')
}
