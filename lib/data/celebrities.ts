export const CELEBRITY_CATEGORIES: CelebrityCategory[] = ['배우', '가수', '스포츠', '정치', '역사', '기업']

/**
 * 이름으로 유명인 검색 (한글 부분 매칭)
 */
export function searchCelebrities(query: string, category?: CelebrityCategory | null, limit = 20): Celebrity[] {
  const q = query.trim().toLowerCase()
  let results = CELEBRITIES

  if (category) {
    results = results.filter((c) => c.category === category)
  }

  if (q) {
    results = results.filter(
      (c) =>
        c.name.toLowerCase().includes(q) ||
        (c.nameEn && c.nameEn.toLowerCase().includes(q)) ||
        c.description.toLowerCase().includes(q)
    )
  }

  return results.slice(0, limit)
}

/**
 * ID로 유명인 조회
 */
export function getCelebrityById(id: string): Celebrity | undefined {
  return CELEBRITIES.find((c) => c.id === id)
}
