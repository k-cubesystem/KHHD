export function injectContext(template: string, variables: Record<string, string>) {
  let result = template.replace('{{PERSONA}}', PERSONA.KIND_MENTOR)
  for (const [key, value] of Object.entries(variables)) {
    result = result.replace(new RegExp(`{{${key}}}`, 'g'), value || '정보 없음')
  }
  return result
}
